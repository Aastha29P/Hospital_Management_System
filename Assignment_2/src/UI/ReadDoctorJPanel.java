private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) DTable.getModel();
            model.setRowCount(0);

            for(Doctor d: docHistory.getDocHistory()){

                Object[] row = new Object[7];
                row[0] = d;
                //row[0] = d.getDocId();
                row[1] = d.getDocName();
                row[2] = d.getDocPhone();
                row[3] = d.getDocGen();
                row[4] = d.getDocDep();
                row[5] = d.getHospId();
                row[6] = d.getHospName();
                
                model.addRow(row);
            }
    }
